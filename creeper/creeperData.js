function randomInt(min, max, includeEnd) {
    return Math.floor(Math.random() * (max - min + (includeEnd ? 1 : 0)));
}

function pickRandom(iterable) {
    let _ = [...iterable]
    return _[randomInt(0, _.length)];
}

